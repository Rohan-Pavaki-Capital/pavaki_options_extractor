"""
Save extraction results (JSON fields + Excel binary) to NeonDB.

Schema lives in schema.sql. Anything not given an explicit column lands in
the `raw_json` JSONB column on the same row, so nothing is ever lost.
"""
from psycopg.types.json import Jsonb

from database.connection import get_connection
from database.setup import ensure_schema


def _safe_num(val):
    """Coerce to numeric or None. NeonDB NUMERIC handles int/float natively."""
    if val is None:
        return None
    if isinstance(val, (int, float)):
        return val
    try:
        return float(val)
    except (TypeError, ValueError):
        return None


def _safe_int(val):
    n = _safe_num(val)
    return int(n) if n is not None else None


def _safe_bool(val):
    if val is None:
        return None
    if isinstance(val, bool):
        return val
    if isinstance(val, str):
        v = val.strip().lower()
        if v in ("true", "yes", "y", "1"):
            return True
        if v in ("false", "no", "n", "0"):
            return False
    return None


def save_extraction(data: dict, excel_bytes: bytes, excel_filename: str) -> int:
    """
    Insert one extraction (parent row + per-plan child rows + per-tranche
    grandchild rows) into NeonDB.

    Args:
        data: Parsed JSON dict produced by the extractor.
        excel_bytes: Raw .xlsx file bytes generated by build_workbook.
        excel_filename: Filename to record (e.g. "eli.xlsx").

    Returns:
        New extractions.id.
    """
    ensure_schema()

    plans = data.get("plans") or []
    meta = data.get("_meta") or {}
    cost = (meta.get("cost") or {}).get("total_cost_usd")
    val_summary = data.get("_validation_summary") or {}
    warnings_cnt = len(val_summary.get("warnings") or [])

    with get_connection() as conn:
        with conn.cursor() as cur:
            cur.execute(
                """
                INSERT INTO extractions (
                    company_name, report_period, currency, reporting_standard,
                    source_pdf, total_plans, extraction_cost_usd,
                    total_pdf_pages, pages_processed,
                    extraction_model, classifier_model, extraction_mode,
                    validation_warnings_cnt,
                    raw_json, excel_file, excel_filename
                )
                VALUES (%s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s)
                RETURNING id
                """,
                (
                    data.get("company_name"),
                    data.get("report_period"),
                    data.get("currency"),
                    data.get("reporting_standard"),
                    meta.get("source_pdf"),
                    len(plans),
                    _safe_num(cost),
                    _safe_int(meta.get("total_pdf_pages")),
                    meta.get("pages_processed") or None,
                    meta.get("model"),
                    (meta.get("detection") or {}).get("stage2_classifier"),
                    meta.get("mode"),
                    warnings_cnt,
                    Jsonb(data),
                    excel_bytes,
                    excel_filename,
                ),
            )
            extraction_id = cur.fetchone()[0]

            for plan in plans:
                plan_id = _insert_plan(cur, extraction_id, plan)
                for idx, tranche in enumerate(plan.get("tranches") or []):
                    _insert_tranche(cur, plan_id, idx, tranche)

    return extraction_id


