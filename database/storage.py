def _insert_plan(cur, extraction_id: int, plan: dict) -> int:
    """Insert one plan row, return the new plans.id."""
    valuation = plan.get("valuation_inputs") or {}
    prior = plan.get("prior_year") or {}

    cur.execute(
        """
        INSERT INTO plans (
            extraction_id,
            plan_name, plan_type, plan_description,
            opening_balance, granted, exercised, forfeited_or_lapsed,
            vested, closing_balance, exercisable_at_period_end,
            weighted_avg_exercise_price, weighted_avg_exercise_price_unit,
            weighted_avg_grant_date_fair_value, fair_value_unit,
            exercise_price_range_low, exercise_price_range_high, exercise_price_range_unit,
            weighted_avg_remaining_contractual_life_years,
            weighted_avg_share_price_at_exercise,
            weighted_avg_share_price_at_exercise_unit,
            units_label, vesting_period_years, performance_period_years, holding_period_years,
            vesting_description, performance_conditions,
            is_nil_cost, is_cash_settled,
            total_contingent_awards, contingent_cash_settled, contingent_equity_settled,
            valuation_model,
            valuation_volatility_pct, valuation_risk_free_rate_pct,
            valuation_dividend_yield_pct, valuation_expected_life_years,
            valuation_stock_price, valuation_stock_price_unit,
            valuation_strike_price,
            valuation_fair_value_per_option, valuation_fair_value_unit,
            prior_opening_balance, prior_granted, prior_exercised, prior_vested,
            prior_forfeited_or_lapsed, prior_closing_balance,
            prior_weighted_avg_exercise_price, prior_weighted_avg_grant_date_fair_value,
            raw_json
        )
        VALUES (
            %s,
            %s, %s, %s,
            %s, %s, %s, %s,
            %s, %s, %s,
            %s, %s,
            %s, %s,
            %s, %s, %s,
            %s,
            %s,
            %s,
            %s, %s, %s, %s,
            %s, %s,
            %s, %s,
            %s, %s, %s,
            %s,
            %s, %s,
            %s, %s,
            %s, %s,
            %s,
            %s, %s,
            %s, %s, %s, %s,
            %s, %s,
            %s, %s,
            %s
        )
        RETURNING id
        """,
        (
            extraction_id,
            plan.get("plan_name"),
            plan.get("plan_type"),
            plan.get("plan_description"),

            _safe_num(plan.get("opening_balance")),
            _safe_num(plan.get("granted")),
            _safe_num(plan.get("exercised")),
            _safe_num(plan.get("forfeited_or_lapsed")),
            _safe_num(plan.get("vested")),
            _safe_num(plan.get("closing_balance")),
            _safe_num(plan.get("exercisable_at_period_end")),

            _safe_num(plan.get("weighted_avg_exercise_price")),
            plan.get("weighted_avg_exercise_price_unit"),
            _safe_num(plan.get("weighted_avg_grant_date_fair_value")),
            plan.get("fair_value_unit"),

            _safe_num(plan.get("exercise_price_range_low")),
            _safe_num(plan.get("exercise_price_range_high")),
            plan.get("exercise_price_range_unit"),

            _safe_num(plan.get("weighted_avg_remaining_contractual_life_years")),
            _safe_num(plan.get("weighted_avg_share_price_at_exercise")),
            plan.get("weighted_avg_share_price_at_exercise_unit"),

            plan.get("units_label"),
            _safe_num(plan.get("vesting_period_years")),
            _safe_num(plan.get("performance_period_years")),
            _safe_num(plan.get("holding_period_years")),

            plan.get("vesting_description"),
            plan.get("performance_conditions"),

            _safe_bool(plan.get("is_nil_cost")),
            _safe_bool(plan.get("is_cash_settled")),

            _safe_num(plan.get("total_contingent_awards")),
            _safe_num(plan.get("contingent_cash_settled")),
            _safe_num(plan.get("contingent_equity_settled")),

            plan.get("valuation_model"),
            _safe_num(valuation.get("volatility_pct")),
            _safe_num(valuation.get("risk_free_rate_pct")),
            _safe_num(valuation.get("dividend_yield_pct")),
            _safe_num(valuation.get("expected_life_years")),
            _safe_num(valuation.get("stock_price")),
            valuation.get("stock_price_unit"),
            _safe_num(valuation.get("strike_price")),
            _safe_num(valuation.get("fair_value_per_option")),
            valuation.get("fair_value_unit"),

            _safe_num(prior.get("opening_balance")),
            _safe_num(prior.get("granted")),
            _safe_num(prior.get("exercised")),
            _safe_num(prior.get("vested")),
            _safe_num(prior.get("forfeited_or_lapsed")),
            _safe_num(prior.get("closing_balance")),
            _safe_num(prior.get("weighted_avg_exercise_price")),
            _safe_num(prior.get("weighted_avg_grant_date_fair_value")),

            Jsonb(plan),
        ),
    )
    return cur.fetchone()[0]


def _insert_tranche(cur, plan_id: int, idx: int, tranche: dict) -> None:
    cur.execute(
        """
        INSERT INTO plan_tranches (
            plan_id, tranche_index,
            grant_date, shares_granted, shares_at_period_end,
            grant_price, grant_price_unit,
            exercise_price, exercise_price_unit,
            vesting_period_years,
            fair_value_per_option, fair_value_unit,
            raw_json
        )
        VALUES (%s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s)
        """,
        (
            plan_id,
            idx,
            tranche.get("grant_date"),
            _safe_num(tranche.get("shares_granted")),
            _safe_num(tranche.get("shares_at_period_end")),
            _safe_num(tranche.get("grant_price")),
            tranche.get("grant_price_unit"),
            _safe_num(tranche.get("exercise_price")),
            tranche.get("exercise_price_unit"),
            _safe_num(tranche.get("vesting_period_years")),
            _safe_num(tranche.get("fair_value_per_option")),
            tranche.get("fair_value_unit"),
            Jsonb(tranche),
        ),
    )
